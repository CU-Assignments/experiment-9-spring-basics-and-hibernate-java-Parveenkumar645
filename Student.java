package com.example.hibernatecrud;

import jakarta.persistence.*;

@Entity
@Table(name = "students")
public class Student {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private int id;

    private String name;
    private int age;

    public Student() {}
    
    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getters & Setters

    @Override
    public String toString() {
        return id + " - " + name + " (" + age + ")";
    }
}
